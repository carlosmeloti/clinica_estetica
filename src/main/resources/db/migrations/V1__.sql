CREATE TABLE agendamento
(
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    data_criacao     TIMESTAMP WITHOUT TIME ZONE,
    data_atualizacao TIMESTAMP WITHOUT TIME ZONE,
    ativo            BOOLEAN,
    paciente_id      BIGINT                                  NOT NULL,
    profissional_id  BIGINT                                  NOT NULL,
    procedimento_id  BIGINT                                  NOT NULL,
    data_hora_inicio TIMESTAMP WITHOUT TIME ZONE             NOT NULL,
    data_hora_fim    TIMESTAMP WITHOUT TIME ZONE             NOT NULL,
    status           VARCHAR(255),
    motivo_consulta  VARCHAR(255),
    valor_previsto   DECIMAL,
    CONSTRAINT pk_agendamento PRIMARY KEY (id)
);

CREATE TABLE evolucoes_clinicas
(
    id                     BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    data_criacao           TIMESTAMP WITHOUT TIME ZONE,
    data_atualizacao       TIMESTAMP WITHOUT TIME ZONE,
    ativo                  BOOLEAN,
    agendamento_id         BIGINT                                  NOT NULL,
    paciente_id            BIGINT                                  NOT NULL,
    profissional_id        BIGINT                                  NOT NULL,
    data_registro          TIMESTAMP WITHOUT TIME ZONE             NOT NULL,
    relato_clinico         TEXT                                    NOT NULL,
    diagnostico_hipotetico VARCHAR(255),
    CONSTRAINT pk_evolucoes_clinicas PRIMARY KEY (id)
);

CREATE TABLE insumo
(
    id                 BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    data_criacao       TIMESTAMP WITHOUT TIME ZONE,
    data_atualizacao   TIMESTAMP WITHOUT TIME ZONE,
    ativo              BOOLEAN,
    nome               VARCHAR(255),
    quantidade_estoque DOUBLE PRECISION,
    unidade_medida     VARCHAR(255),
    CONSTRAINT pk_insumo PRIMARY KEY (id)
);

CREATE TABLE local_aplicacao
(
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    data_criacao     TIMESTAMP WITHOUT TIME ZONE,
    data_atualizacao TIMESTAMP WITHOUT TIME ZONE,
    ativo            BOOLEAN,
    nome             VARCHAR(255)                            NOT NULL,
    pre_definido     BOOLEAN                                 NOT NULL,
    CONSTRAINT pk_localaplicacao PRIMARY KEY (id)
);

CREATE TABLE paciente
(
    id                 BIGINT NOT NULL,
    observacoes_gerais VARCHAR(255),
    tipo_sanguineo     VARCHAR(255),
    nome_responsavel   VARCHAR(255),
    CONSTRAINT pk_paciente PRIMARY KEY (id)
);

CREATE TABLE pessoa
(
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    data_criacao     TIMESTAMP WITHOUT TIME ZONE,
    data_atualizacao TIMESTAMP WITHOUT TIME ZONE,
    ativo            BOOLEAN,
    nome             VARCHAR(255),
    cpf              VARCHAR(255),
    email            VARCHAR(255),
    telefone         VARCHAR(255),
    data_nascimento  date,
    logradouro       VARCHAR(255),
    numero           VARCHAR(255),
    complemento      VARCHAR(255),
    bairro           VARCHAR(255),
    cidade           VARCHAR(255),
    estado           VARCHAR(255),
    cep              VARCHAR(255),
    CONSTRAINT pk_pessoa PRIMARY KEY (id)
);

CREATE TABLE procedimento
(
    id               BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    data_criacao     TIMESTAMP WITHOUT TIME ZONE,
    data_atualizacao TIMESTAMP WITHOUT TIME ZONE,
    ativo            BOOLEAN,
    nome             VARCHAR(255)                            NOT NULL,
    preco_sugerido   DECIMAL,
    duracao_minutos  INTEGER,
    CONSTRAINT pk_procedimento PRIMARY KEY (id)
);

CREATE TABLE usuarios
