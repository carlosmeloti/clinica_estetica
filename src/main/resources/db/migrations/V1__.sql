    senha                 VARCHAR(255)                            NOT NULL,
    nome                  VARCHAR(255)                            NOT NULL,
    email                 VARCHAR(255),
    perfil                VARCHAR(255),
    registro_profissional VARCHAR(255),
    CONSTRAINT pk_usuarios PRIMARY KEY (id)
);

